#include <unistd.h>

void	rush(int x, int y);

int	main(void)
{
	int	x;
	int	y;

	x = 5;
	y = 5;
	if (x <= 0 || y <= 0 || x > 2147483647 || y > 2147483647)
	{
		write(1, "Invalid Values", 16);
		return (1);
	}
	rush(x, y);
	return (0);
}
